class AuntSue
{
    public int Number { get; }
    public int Children { get; }
    public int Cats { get; }
    public int Samoyeds { get; }
    public int Pomeranians { get; }
    public int Akitas { get; }
    public int Vizslas { get; }
    public int Goldfish { get; }
    public int Trees { get; }
    public int Cars { get; }
    public int Perfumes { get; }

    public AuntSue(
        int number,
        int children,
        int cats,
        int samoyeds,
        int pomeranians,
        int akitas,
        int vizslas,
        int goldfish,
        int trees,
        int cars,
        int perfumes)
    {
        Number = number;
        Children = children;
        Cats = cats;
        Samoyeds = samoyeds;
        Pomeranians = pomeranians;
        Akitas = akitas;
        Vizslas = vizslas;
        Goldfish = goldfish;
        Trees = trees;
        Cars = cars;
        Perfumes = perfumes;
    }
}
